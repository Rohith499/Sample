package Demo;

public class IncrementDecrement {

	public static void main(String[] args)
	{
		//Pre Increment
		int x=10;
		int y=++x;
		System.out.println(x +" "+ y);
		
		//Post Increment
		
		int ax=10;
		int ay=x++;
		System.out.println(ax +" "+ ay);
		
		//Pre Decrement
		int j=10;
		int k=--j;
		System.out.println(k +" "+ j);
				
		//Post Decrement
				
		int aj=10;
		int ak=j--;
		System.out.println(aj +" "+ ak);
				
		
		

	}

}
